#include "ft_printf.h"

int		ft_atoi(char const *s)
{
	int		res;
	int		i;

	i = 0;
	res = 0;
	while ((9 <= s[i] && s[i] <= 13) || s[i] == 32)
		i++;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] && '0' <= s[i] && s[i] <= '9')
	{
		res = res * 10 + s[i] - '0';
		i++;
	}
	i = 0;
	while ((9 <= s[i] && s[i] <= 13) || s[i] == 32)
		i++;
	if (s[i] == '-')
		res = -res;
	return (res);
}
