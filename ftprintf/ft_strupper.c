#include "ft_printf.h"

void		ft_strupper(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ('a' <= str[i] && str[i] <= 'z')
			str[i] = str[i] - 'a' + 'A';
		i++;
	}
}
